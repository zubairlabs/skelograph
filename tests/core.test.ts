async function makeTmp(name: string): Promise<string> {
  return mkdtemp(join(tmpdir(), `skelograph-${name}-`));
}

test("ignore rules match directories and globs", () => {
  const rules = [parseIgnoreRule("node_modules/"), parseIgnoreRule("*.generated.ts")];
  assert.equal(isIgnored(rules, "node_modules", true), true);
  assert.equal(isIgnored(rules, "pkg/node_modules", true), true);
  assert.equal(isIgnored(rules, "src/foo.generated.ts", false), true);
  assert.equal(isIgnored(rules, "src/foo.ts", false), false);
});

test("scan respects .gitignore and skips generated files", async () => {
  const root = await makeTmp("scan");
  try {
    await mkdir(join(root, "src"));
    await writeFile(join(root, ".gitignore"), "src/ignored.ts\n", "utf8");
    await writeFile(join(root, "src", "index.ts"), "export function run() {}\n", "utf8");
    await writeFile(join(root, "src", "ignored.ts"), "export function nope() {}\n", "utf8");
    await writeFile(
      join(root, "src", "generated.ts"),
      "// @generated by build\nexport const x = 1;\n",
      "utf8",
    );
    await writeFile(join(root, ".env"), "TOKEN=secret\n", "utf8");
    await writeFile(join(root, "package.json"), JSON.stringify({ name: "demo" }), "utf8");

    const result = await scan(root);
    const paths = result.files.map((f) => f.relativePath).sort();
    assert.deepEqual(paths, ["src/index.ts"]);
    assert.equal(result.stats.skippedSensitive, 1);
    assert.equal(result.stats.skippedGenerated, 1);
  } finally {
    await rm(root, { recursive: true, force: true });
  }
});

