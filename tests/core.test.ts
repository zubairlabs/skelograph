test("claude install injects hard rules at top and survives re-runs", async () => {
  const root = await makeTmp("claude");
  try {
    await writeFile(join(root, "CLAUDE.md"), "# Existing Notes\n\nKeep this.\n", "utf8");

    const dryRun = await installClaude({ root, dryRun: true });
    assert.equal(dryRun.changed.length, 2);

    const result = await installClaude({ root });
    assert.equal(result.changed.length, 2);

    const claudeMd = await readFile(join(root, "CLAUDE.md"), "utf8");
    assert.match(claudeMd, /# Existing Notes/);
    assert.match(claudeMd, /HARD RULES/);
    assert.match(claudeMd, /ALWAYS:/);
    assert.match(claudeMd, /NEVER:/);

    const indexOfHeading = claudeMd.indexOf("# Existing Notes");
    const indexOfRules = claudeMd.indexOf("HARD RULES");
    assert.ok(indexOfRules > indexOfHeading, "rules block must appear after the H1 heading");
    const indexOfKeep = claudeMd.indexOf("Keep this");
    assert.ok(indexOfRules < indexOfKeep, "rules block must appear before existing body content");

    const second = await installClaude({ root });
    assert.equal(second.changed.length, 0);
  } finally {
    await rm(root, { recursive: true, force: true });
  }
});
